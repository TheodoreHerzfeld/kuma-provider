package main

import (
	"context"
	"flag"
	"log"
	"strings"

	"github.com/hashicorp/terraform-plugin-framework/providerserver"

	"terraform-provider-uptime-kuma/internal/provider"
)

var (
	// these will be set by the goreleaser configuration
	// to appropriate values for the compiled binary.
	version string = "dev"

	// goreleaser can pass other information to the main package, such as the specific commit
	// https://goreleaser.com/cookbooks/using-main.version/
)

// helper funcs
func cleanString(dirty string) string {

	tout := strings.Trim(dirty, "\"")
	tout = strings.TrimLeft(tout, "<")
	tout = strings.TrimRight(tout, ">")

	return tout
}

func main() {
	var debug bool

	flag.BoolVar(&debug, "debug", false, "set to true to run the provider with support for debuggers like delve")
	flag.Parse()

	opts := providerserver.ServeOpts{
		// NOTE: This is not a typical Terraform Registry provider address,
		// such as registry.terraform.io/hashicorp/hashicups. This specific
		// provider address is used in these tutorials in conjunction with a
		// specific Terraform CLI configuration for manual development testing
		// of this provider.
		Address: "hashicorp.com/redted/uptime-kuma",
		Debug:   debug,
	}

	err := providerserver.Serve(context.Background(), provider.New(version), opts)

	if err != nil {
		log.Fatal(err.Error())
	}
}
